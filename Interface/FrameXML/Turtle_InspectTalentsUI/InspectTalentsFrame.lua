function fixDescriptionLength(d)

    local lineLength = 40

    if string.len(d) <= lineLength then
        return d
    end
    d = TWTalent.replace(d, '. ', '.')
    d = TWTalent.replace(d, '.', '.\n')

    return d
end

function TWTalentDescription(i, j)

    if not talentDescriptions[inspectCom.SPEC.class .. inspectCom.SPEC[i].name] then
        return ''
    end

    for _, d in talentDescriptions[inspectCom.SPEC.class .. inspectCom.SPEC[i].name] do
        if d.n == inspectCom.SPEC[i][j].name then
            return fixDescriptionLength(d.d[inspectCom.SPEC[i][j].rank > 0 and inspectCom.SPEC[i][j].rank or 1])
        end
    end
    return ''
end

