function TWTalent.replace(text, search, replace)
    if search == replace then
        return text
    end
    local searchedtext = ""
    local textleft = text
    while __ins_find(textleft, search, 1, true) do
        searchedtext = searchedtext .. strsub(textleft, 1, strfind(textleft, search, 1, true) - 1) .. replace
        textleft = strsub(textleft, __ins_find(textleft, search, 1, true) + strlen(search))
    end
    if strlen(textleft) > 0 then
        searchedtext = searchedtext .. textleft
    end
    return searchedtext
end


